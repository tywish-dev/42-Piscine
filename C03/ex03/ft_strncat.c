#include <unistd.h>

char	*ft_strncat(char *dest, char *src, unsigned int nb)
{
	int		dest_len;
	int		i;

	dest_len = 0;
	i = 0;
	while (dest[dest_len])
		dest_len++;
	while (i < nb && src[i])
	{
		dest[dest_len + i] = src[i];
		i++;
	}
	dest[dest_len + i] = '\0';
	return (dest);
}
