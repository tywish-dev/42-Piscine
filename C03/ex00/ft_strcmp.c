#include <unistd.h>

int	ft_strcmp(char *s1, char *s2)
{
	int		i;

	i = 0;
	while (*s1 == *s2 && s1[i] != '\0' || s2[i] != '\0')
	{
		s1++;
		s2++;
		i++;
	}
	return (*s1 - *s2);
}
